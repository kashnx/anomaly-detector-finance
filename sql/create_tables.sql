CREATE TABLE financials (
    Segment VARCHAR(50),
    Country VARCHAR(50),
    Product VARCHAR(50),
    Discount_Band VARCHAR(30),
    Units_Sold FLOAT,
    Manufacturing_Price FLOAT,
    Sale_Price FLOAT,
    Gross_Sales FLOAT,
    Discounts FLOAT,
    Sales FLOAT,
    COGS FLOAT,
    Profit FLOAT,
    Date DATE,
    Month_Number INTEGER,
    Month_Name VARCHAR(20),
    Year INTEGER
);
